using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodInOOP
{
     class Person
    {
        public string name;
        public int age;

        //constructor
        public Person()
        {
            name = name;
            age = age;
        }

        //overloading constructor
        public Person(string name, int age)
        {
            this.name = name;
            this.age = age;
        }

    }
}
